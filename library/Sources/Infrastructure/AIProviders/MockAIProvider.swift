import Foundation
import Domain

/// Mock AI Provider for testing without API credentials
/// Returns pre-generated responses for demonstration purposes
public final class MockAIProvider: AIProviderPort, Sendable {
    public var providerName: String { "Mock" }
    public var modelName: String { "mock-model-v1" }
    public var contextWindowSize: Int { 128000 }

    public init() {}

    public func generateText(
        prompt: String,
        temperature: Double
    ) async throws -> String {
        return generateMockResponse(for: prompt)
    }

    public func streamText(
        prompt: String,
        temperature: Double
    ) async throws -> AsyncStream<String> {
        let response = generateMockResponse(for: prompt)

        return AsyncStream { continuation in
            Task {
                // Simulate streaming by chunking
                for chunk in response.split(separator: " ") {
                    continuation.yield(String(chunk) + " ")
                    try? await Task.sleep(nanoseconds: 10_000_000) // 10ms delay
                }
                continuation.finish()
            }
        }
    }

    private func generateMockResponse(for prompt: String) -> String {
        """
        ## Mock PRD Section

        This is a demonstration response generated by the Mock AI Provider for testing purposes.

        ### Overview
        The system will provide the requested functionality as specified in the requirements.

        ### Key Features
        - Feature 1: Core functionality implementation
        - Feature 2: User interface components
        - Feature 3: Data management capabilities

        ### Technical Requirements
        - Backend: Swift/Vapor microservices architecture
        - Database: PostgreSQL with Fluent ORM
        - Frontend: React with TypeScript
        - API: RESTful endpoints with JSON payloads

        ### Success Criteria
        - All functional requirements met
        - Performance targets achieved (< 200ms response time)
        - Security standards implemented (OAuth 2.0, JWT tokens)
        - Test coverage > 80%

        *Note: This is a mock response for demonstration. Connect real AI provider for production use.*
        """
    }
}
